/**
 * Notion MCP Wrapper
 *
 * Provides methods for creating pages and databases in Notion via MCP.
 * Falls back to returning markdown content for manual creation when
 * the MCP server is unavailable.
 */

import { callTool, isServerAvailable } from './client';

// ---------------------------------------------------------------------------
// Types
// ---------------------------------------------------------------------------

export interface NotionPageResult {
  id: string;
  url: string;
  isMock: boolean;
  fallbackMarkdown?: string;
  warning?: string;
}

export interface NotionDatabaseResult {
  id: string;
  url: string;
  isMock: boolean;
  fallbackMarkdown?: string;
  warning?: string;
}

export interface NotionProperty {
  name: string;
  type: 'title' | 'rich_text' | 'number' | 'select' | 'multi_select' | 'date' | 'url' | 'email' | 'checkbox';
  options?: string[];
}

// ---------------------------------------------------------------------------
// Configuration
// ---------------------------------------------------------------------------

const NOTION_MCP_URL = process.env.NOTION_MCP_URL ?? 'http://localhost:3103';

// ---------------------------------------------------------------------------
// Public API
// ---------------------------------------------------------------------------

/**
 * Create a page in Notion.
 *
 * @param title     Page title
 * @param content   Page content (markdown)
 * @param parentId  Optional parent page or database ID
 */
export async function createPage(
  title: string,
  content: string,
  parentId?: string,
): Promise<NotionPageResult> {
  try {
    const available = await isServerAvailable(NOTION_MCP_URL, { timeoutMs: 3000 });
    if (!available) {
      return buildFallbackPage(title, content, parentId);
    }

    const params: Record<string, unknown> = { title, content };
    if (parentId) {
      params.parentId = parentId;
    }

    const result = await callTool(
      NOTION_MCP_URL,
      'notion_create_page',
      params,
      { timeoutMs: 10_000 },
    );

    if (result.isError) {
      console.warn(`[mcp/notion] createPage error: ${result.errorMessage}`);
      return buildFallbackPage(title, content, parentId);
    }

    const data = result.content as Record<string, unknown>;
    return {
      id: String(data.id ?? 'unknown'),
      url: String(data.url ?? ''),
      isMock: false,
    };
  } catch (err) {
    console.warn('[mcp/notion] createPage exception:', err);
    return buildFallbackPage(title, content, parentId);
  }
}

/**
 * Create a database in Notion.
 *
 * @param title      Database title
 * @param properties Array of property definitions
 * @param parentId   Optional parent page ID
 */
