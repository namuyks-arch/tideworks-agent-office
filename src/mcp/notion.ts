// ---------------------------------------------------------------------------
// Fallback Builders
// ---------------------------------------------------------------------------

function buildFallbackPage(
  title: string,
  content: string,
  parentId?: string,
): NotionPageResult {
  const fallbackMarkdown = [
    `# ${title}`,
    '',
    parentId ? `> Parent: ${parentId}` : '',
    '',
    content,
    '',
    '---',
    '*This page was generated by Tideworks Agent Office. Create it manually in Notion.*',
  ]
    .filter(Boolean)
    .join('\n');

  return {
    id: `mock-${Date.now()}`,
    url: '',
    isMock: true,
    fallbackMarkdown,
    warning: 'Notion MCP server unavailable. Markdown content ready for manual creation.',
  };
}

function buildFallbackDatabase(
  title: string,
  properties: NotionProperty[],
): NotionDatabaseResult {
  const propertyLines = properties.map((p) => {
    const optionsStr = p.options?.length ? ` (options: ${p.options.join(', ')})` : '';
    return `| ${p.name} | ${p.type}${optionsStr} |`;
  });

  const fallbackMarkdown = [
    `# Database: ${title}`,
    '',
    '## Properties',
    '| Name | Type |',
    '|------|------|',
    ...propertyLines,
    '',
    '---',
    '*This database was generated by Tideworks Agent Office. Create it manually in Notion.*',
  ].join('\n');

  return {
    id: `mock-${Date.now()}`,
    url: '',
    isMock: true,
    fallbackMarkdown,
    warning: 'Notion MCP server unavailable. Database schema ready for manual creation.',
  };
}
